Adafruit_SSD1306 display1(OLED_RESET);
Adafruit_SSD1306 display2(OLED_RESET);

void setup()
{
  Serial.begin(9600);
  waitFor(Serial.isConnected, 5000);
  display1.begin(SSD1306_SWITCHCAPVCC, ADDY);
  display2.begin(SSD1306_SWITCHCAPVCC, ADDY2);
}

void loop()
{
  display1.clearDisplay();
  display2.clearDisplay();
  display1.drawBitmap(0, 0, open, FYFX5S_BMPWIDTH, FYFX5S_BMPHEIGHT, WHITE);
  display2.drawBitmap(0, 0, open, FYFX5S_BMPWIDTH, FYFX5S_BMPHEIGHT, WHITE);
  display1.display();
  display2.display();
  if ((millis() - lastTime) > 5000)
  {
    display1.clearDisplay();
    display2.clearDisplay();
    display1.drawBitmap(0, 0, closed, F4ZDQK_BMPWIDTH, F4ZDQK_BMPHEIGHT, WHITE);
    display2.drawBitmap(0, 0, closed, F4ZDQK_BMPWIDTH, F4ZDQK_BMPHEIGHT, WHITE);
    display1.display();
    display2.display();
    lastTime = millis();
  }
}
